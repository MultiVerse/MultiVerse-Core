package com.onarandombox.multiverse.core.minecraft;

public enum PortalType {

    /**
     * No portals are allowed.
     */
    NONE,
    /**
     * All portal types are allowed.
     */
    ALL,
    /**
     * Only Nether style portals are allowed.
     */
    NETHER,
    /**
     * Only Ender style portals are allowed.
     */
    END;
}
